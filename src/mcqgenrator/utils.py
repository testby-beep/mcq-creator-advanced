def export_to_csv_bytes(df: pd.DataFrame) -> bytes:
    return df.to_csv(index=False).encode("utf-8")


def export_to_docx_bytes(df: pd.DataFrame, review: str, subject: str, tone: str) -> bytes:
    doc = Document()
    doc.add_heading(f"{subject} Quiz", level=1)
    doc.add_paragraph(f"Difficulty / Tone: {tone}")
    doc.add_paragraph("")

    for i, row in df.iterrows():
        p = doc.add_paragraph()
        run = p.add_run(f"Q{i}. {row['Question']}")
        run.bold = True
        run.font.size = Pt(12)

        if row["Choices"] != "—":
            choices_p = doc.add_paragraph(str(row["Choices"]).replace(" || ", "\n"))
            choices_p.paragraph_format.left_indent = Pt(18)

        ans_p = doc.add_paragraph()
        ans_run = ans_p.add_run(f"Correct answer: {row['Correct']}")
        ans_run.italic = True

        if row.get("Explanation"):
            exp_p = doc.add_paragraph(f"Why: {row['Explanation']}")
            exp_p.paragraph_format.left_indent = Pt(18)

        doc.add_paragraph("")

    if review:
        doc.add_heading("Expert Review", level=2)
        doc.add_paragraph(review)

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


class _QuizPDF(FPDF):
    def header(self):
        pass


def _register_pdf_font(pdf) -> str:
    """Register the bundled Unicode TTF font if the files are present;
    otherwise fall back to the built-in Helvetica (Latin-1 only, but
    always available so export never hard-fails)."""
    if all(os.path.isfile(p) for p in _UNICODE_FONT_FILES.values()):
        try:
            for style, path in _UNICODE_FONT_FILES.items():
                pdf.add_font(_UNICODE_FONT_FAMILY, style, path)
            return _UNICODE_FONT_FAMILY
        except Exception:
            logger.exception("Failed to register Unicode PDF font, falling back to Helvetica")
    else:
        logger.warning("Unicode PDF fonts not found under %s -- falling back to Helvetica.", _FONTS_DIR)
    return "Helvetica"


def _line(pdf, h, text):
    """multi_cell that always returns the cursor to the left margin / next
    line, and never lets an unsupported character crash the whole export.
    DejaVu Sans covers Latin (incl. accents), Greek, Cyrillic, and common
    math symbols; if a character still isn't supported (e.g. Arabic,
    Devanagari, CJK -- DejaVu doesn't include these), that one line
    degrades to an ASCII-safe version instead of raising."""
    pdf.set_x(pdf.l_margin)
    try:
        pdf.multi_cell(0, h, text)
    except Exception:
        logger.warning("PDF font couldn't render some characters in a line; using ASCII fallback for it.")
        safe_text = text.encode("ascii", "replace").decode("ascii")
        pdf.set_x(pdf.l_margin)
        pdf.multi_cell(0, h, safe_text)


def export_to_pdf_bytes(df: pd.DataFrame, review: str, subject: str, tone: str) -> bytes:
    pdf = _QuizPDF()
    font = _register_pdf_font(pdf)
    pdf.add_page()
    pdf.set_font(font, "B", 16)
    _line(pdf, 10, f"{subject} Quiz")
    pdf.set_font(font, "", 11)
    _line(pdf, 8, f"Difficulty / Tone: {tone}")
    pdf.ln(4)

    for i, row in df.iterrows():
        pdf.set_font(font, "B", 11)
        _line(pdf, 7, f"Q{i}. {row['Question']}")
        pdf.set_font(font, "", 10)
        if row["Choices"] != "—":
            for choice in str(row["Choices"]).split(" || "):
                _line(pdf, 6, f"   {choice}")
        pdf.set_font(font, "I", 10)
        _line(pdf, 6, f"   Correct: {row['Correct']}")
        if row.get("Explanation"):
            _line(pdf, 6, f"   Why: {row['Explanation']}")
        pdf.ln(2)

    if review:
        pdf.ln(4)
        pdf.set_font(font, "B", 13)
        _line(pdf, 8, "Expert Review")
        pdf.set_font(font, "", 10)
        _line(pdf, 6, review)

    return bytes(pdf.output())


def export_to_gift_bytes(quiz_dict: dict, subject: str) -> bytes:
    """Moodle GIFT format -- importable by Moodle and several other LMSes."""
    def esc(s):
        return str(s).replace("{", "(").replace("}", ")").replace("~", "-").replace("=", "-")

    lines = [f"// {subject} quiz exported from AnswerKey Studio", ""]
    for i, (_, q) in enumerate(quiz_dict.items(), start=1):
        qtype = _infer_type(q)
        question = esc(_question_text(q))

        if qtype == "mcq" and "options" in q:
            correct_letter = str(q.get("correct", "")).strip().lower()
            opt_lines = []
            for letter, text in q["options"].items():
                prefix = "=" if letter.strip().lower() == correct_letter else "~"
                opt_lines.append(f"{prefix}{esc(text)}")
            lines.append(f"::Q{i}:: {question} {{")
            lines.extend(opt_lines)
            lines.append("}")
        elif qtype == "true_false":
            correct = str(q.get("correct", "")).strip().lower()
            tf = "TRUE" if correct.startswith("t") else "FALSE"
            lines.append(f"::Q{i}:: {question} {{{tf}}}")
        elif qtype == "fill_blank":
            answer = esc(q.get("correct", ""))
            if "____" in question:
                lines.append(f"::Q{i}:: {question.replace('____', '{=' + answer + '}')}")
            else:
                lines.append(f"::Q{i}:: {question} {{={answer}}}")
        else:  # short_answer
            answer = esc(q.get("correct", ""))
            lines.append(f"::Q{i}:: {question} {{={answer}}}")

        lines.append("")

    return "\n".join(lines).encode("utf-8")


def export_to_quizlet_tsv_bytes(quiz_dict: dict) -> bytes:
    """Tab-separated term/definition pairs -- pastes directly into Quizlet's
    'Import' flow (Term / Definition, row per line)."""
    rows = []
    for _, q in quiz_dict.items():
        qtype = _infer_type(q)
        question = _question_text(q)

        if qtype == "mcq" and "options" in q:
            opts = "; ".join(f"{k}) {v}" for k, v in q["options"].items())
            term = f"{question} [{opts}]"
        else:
            term = question

        definition = str(q.get("correct", ""))
        term = term.replace("\t", " ").replace("\n", " ")
        definition = definition.replace("\t", " ").replace("\n", " ")
        rows.append(f"{term}\t{definition}")

    return "\n".join(rows).encode("utf-8")