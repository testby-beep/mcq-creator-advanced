# Bundled Unicode font (DejaVu Sans) so PDF export doesn't crash on
# non-Latin-1 characters -- math symbols (Δ, π...), Greek, Cyrillic, and
# accented Latin all work. DejaVu doesn't cover Arabic/Devanagari/CJK; for
# those languages Word/CSV/JSON export (which are fully Unicode-safe)
# should be used instead -- see the fallback in _line() below.
_FONTS_DIR = os.path.normpath(
    os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "..", "fonts")
)
_UNICODE_FONT_FAMILY = "DejaVu"
_UNICODE_FONT_FILES = {
    "": os.path.join(_FONTS_DIR, "DejaVuSans.ttf"),
    "B": os.path.join(_FONTS_DIR, "DejaVuSans-Bold.ttf"),
    "I": os.path.join(_FONTS_DIR, "DejaVuSans-Oblique.ttf"),
}


def read_file(file):
    """Extract raw text from an uploaded .pdf or .txt file."""
    name = getattr(file, "name", "")

    if name.lower().endswith(".pdf"):
        try:
            reader = PdfReader(file)
            text = ""
            for page in reader.pages:
                extracted = page.extract_text() or ""
                text += extracted
            if not text.strip():
                raise ValueError(
                    "No extractable text found (the PDF may be scanned/image-based)."
                )
            return text
        except Exception as e:
            logger.exception("Failed to read PDF")
            raise Exception(f"Error reading the PDF file: {e}")

    elif name.lower().endswith(".txt"):
        try:
            return file.read().decode("utf-8")
        except Exception as e:
            logger.exception("Failed to read TXT")
            raise Exception(f"Error reading the text file: {e}")

    else:
        raise Exception("Unsupported file format. Only PDF and TXT files are supported.")


def combine_texts(texts: list) -> str:
    """Join multiple source documents into one body of text with clear separators."""
    parts = []
    for i, t in enumerate(texts, start=1):
        if t and t.strip():
            parts.append(f"--- Source {i} ---\n{t.strip()}")
    return "\n\n".join(parts)


def _strip_code_fences(s: str) -> str:
    """LLMs sometimes wrap JSON in ```json ... ``` fences -- strip those off."""
    s = s.strip()
    s = re.sub(r"^```(?:json)?\s*", "", s)
    s = re.sub(r"\s*```$", "", s)
    return s.strip()


def _infer_type(value: dict) -> str:
    if "type" in value:
        return value["type"]
    # backward compatibility with the older (pre question-type) schema
    if "options" in value:
        return "mcq"
    if "statement" in value:
        return "true_false"
    return "short_answer"


def _question_text(value: dict) -> str:
    return value.get("question", value.get("mcq", value.get("statement", "")))


def get_table_data(quiz_str):
    """
    Parse the model's JSON quiz output into a list of dict rows suitable for
    a dataframe/table. Supports mcq / true_false / fill_blank / short_answer,
    including quizzes generated by the older schema.
    Returns False on failure (caller should check for that).
    """
    try:
        cleaned = _strip_code_fences(quiz_str)
        quiz_dict = json.loads(cleaned)
        rows = []

        for _, value in quiz_dict.items():
            qtype = _infer_type(value)
            question_text = _question_text(value)

            if qtype == "mcq" and "options" in value:
                choices = " || ".join(
                    f"{opt}-> {opt_val}" for opt, opt_val in value["options"].items()
                )
            elif qtype == "true_false":
                choices = "True || False"
            else:
                choices = "—"

            rows.append(
                {
                    "Type": qtype.replace("_", " ").title(),
                    "Question": question_text,
                    "Choices": choices,
                    "Correct": value.get("correct", ""),
                    "Bloom Level": value.get("bloom_level", ""),
                    "Explanation": value.get("explanation", ""),
                }
            )

        return rows

    except Exception:
        logger.exception("Failed to parse quiz JSON")
        return False


def quiz_dict_from_str(quiz_str):
    """Return the raw parsed quiz dict (used for interactive self-check UI)."""
    try:
        return json.loads(_strip_code_fences(quiz_str))
    except Exception:
        logger.exception("Failed to parse quiz dict")
        return None


def estimate_min_word_count(mcq_count: int) -> int:
    """A rough heuristic: need enough source text to support the requested
    number of questions without the model repeating/inventing content."""
    return max(80, mcq_count * 25)


